# Write a Python program to get the maximum and minimum values of a dictionary

dict1={"a":10,"b":20,"c":30,"d":5}

max1=max(dict1.values()) #max is used along with value to find the maximum value

min1=min(dict1.values()) #min is used along with value to find the minumum value

print("Maximum value:", max1)
print("Minimum value:", min1)
